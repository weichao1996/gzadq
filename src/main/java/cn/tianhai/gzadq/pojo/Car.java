package cn.tianhai.gzadq.pojo;

public class Car {
	/**
	 * 车型信息
	 */
	private int cid;
	private double A;
	private int m;
	private double kcs;
	private double kcl;
	private String name;
	private double Lc;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public double getA() {
		return A;
	}
	public void setA(double a) {
		A = a;
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	public double getKcs() {
		return kcs;
	}
	public void setKcs(double kcs) {
		this.kcs = kcs;
	}
	public double getKcl() {
		return kcl;
	}
	public void setKcl(double kcl) {
		this.kcl = kcl;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getLc() {
		return Lc;
	}
	public void setLc(double lc) {
		Lc = lc;
	}
	
	
	
}
